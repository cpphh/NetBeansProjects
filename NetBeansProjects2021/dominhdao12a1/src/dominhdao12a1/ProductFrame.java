    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        int selectedIndex = tblProduct.getSelectedRow();
        if(selectedIndex >= 0) {
            Product std = productList.get(selectedIndex);
            
            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this item?");
            System.out.println("option : " + option);
            
            if(option == 0) {
                ProductModify.delete(std.getId());
                
                showStudent();
            }
        }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void btntimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntimkiemActionPerformed
        String input = JOptionPane.showInputDialog(this, "Nhập đầy đủ họ tên tìm kiếm");
        if(input != null && input.length() > 0) {
            productList = ProductModify.findByTensp(input);
            
            tableModel.setRowCount(0);
        
            productList.forEach((student) -> {
                tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, student.getTensp(), 
                    student.getHangsx(),student.getSoluong(), student.getNgaynhap(), student.getGiaban()});
            });
        } else {
            showStudent();
        }
    }//GEN-LAST:event_btntimkiemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
         Product st = getSelected();
        if (st==null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 sinh viên để sửa!");
        }else{
            String tensp = txttensp.getText().trim();
            String hangsx = cbhangsx.getSelectedItem().toString();
            int soluong = Integer.parseInt(txtsoluong.getText().trim());
            String ngaynhap = txtngaynhap.getText();
            String giaban = txtgiaban.getText();
            st.setTensp(tensp);
            st.setSoluong(soluong);
            st.setNgaynhap(ngaynhap);
            st.setHangsx(hangsx);
            st.setGiaban(giaban);

            ProductModify.update(st);
            showStudent();
        
        }
        
        
    }//GEN-LAST:event_btnsuaActionPerformed
 public Product getSelected(){
        int idx=tblProduct.getSelectedRow();
        if(idx==-1)
            return null;
        else{
            String tensp=tblProduct.getModel().getValueAt(idx,1).toString();
            int id=Integer.parseInt(tblProduct.getModel().getValueAt(idx,0).toString());
            String hangsx=tblProduct.getModel().getValueAt(idx,2).toString();
            int soluong=Integer.parseInt(tblProduct.getModel().getValueAt(idx,3).toString());
            String ngaynhap=tblProduct.getModel().getValueAt(idx,4).toString();
            String giaban=tblProduct.getModel().getValueAt(idx,5).toString();
            return new Product(id,tensp,hangsx,ngaynhap,giaban,soluong);            
        }
    }
