    private void showStudent() {
        productList = ProductModify.findAll();
        
        tableModel.setRowCount(0);
        
        productList.forEach((student) -> {
            tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, student.getTensp(), 
                student.getHangsx(),student.getSoluong(), student.getNgaynhap(), student.getGiaban()});
        });
    }
